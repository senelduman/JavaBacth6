package com.syntax.class03;

public class ShortHandOperators {
public static void main(String[] args) {
	
	int num =200;
	num =num+300;
	System.out.println(num);
	
	num=num-100;
	System.out.println(num);
	
	num-=100;//100
	System.out.println(num);
	
	num+=50;
	System.out.println(num);
	
	num /=7;
	System.out.println(num);
   
	num*=10;
	System.out.println(num);
	
	num%=5;
	System.out.println(num);
	



}
}
