package com.syntax.class04;

public class NestedStatement {
		public static void main(String[] args) {
			int  candy=2;
			if (candy==1) {
				System.out.println("One candy");
				System.out.println("I m going to give it to my daughter");
			}else if (candy ==2) {
				System.out.println("Two candies");
				System.out.println("I m going to eat one and give one to my daughter");
			}else if (candy==3) {
				System.out.println(" Three candies");
				System.out.println(" I m going to eat one and give one to my daughter and my best friend");
			}else {
				System.out.println("I will share with my syntax classmates ");
			}
		}
}
