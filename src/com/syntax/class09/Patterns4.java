package com.syntax.class09;

public class Patterns4 {
public static void main(String[] args) {
	for (int i = 1; i<=5; i++) {
		for (int j =5 ; j>=1; j --) {
			System.out.print(j);
		}
		System.out.println();
	}

	for (int x=5; x>0; x--) {
		for (int y=5;y>0;y--) {
			System.out.print(x+"");
		}
		System.out.println();

}
}
}
